CREATE TABLE IF NOT EXISTS badge_series
(
	uuid TEXT PRIMARY KEY,
	code TEXT NOT NULL,
	position INTEGER NOT NULL DEFAULT 0,
	CONSTRAINT code_unique UNIQUE (code)
);

CREATE TABLE IF NOT EXISTS badges
(
	uuid TEXT PRIMARY KEY,
	series_uuid TEXT NOT NULL,
	code TEXT NOT NULL,
	image TEXT NOT NULL,
	position INTEGER NOT NULL DEFAULT 0,
	CONSTRAINT code_unique UNIQUE (code),
	FOREIGN KEY(series_uuid) REFERENCES badge_series(uuid)
);

CREATE TABLE IF NOT EXISTS member_badges
(
	id INTEGER PRIMARY KEY AUTOINCREMENT,
	member_uuid TEXT NOT NULL,
	badge_uuid TEXT NOT NULL,
	awarded_at INTEGER NOT NULL DEFAULT 0,
	awarded_by TEXT,
	CONSTRAINT member_badge_unique UNIQUE (member_uuid, badge_uuid),
	FOREIGN KEY(member_uuid) REFERENCES members(uuid),
	FOREIGN KEY(badge_uuid) REFERENCES badges(uuid)
);

